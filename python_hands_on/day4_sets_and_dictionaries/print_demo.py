first_name = "Albert"
middle_name = "Bond"
last_name = "Einstein"

# sep = separates objects passed in print function
# end = ends the print statement

print(first_name, middle_name, last_name)
print(first_name, middle_name, last_name, sep='*')
print(first_name, middle_name, last_name, sep='*', end='\n')
print("Hello! How are you?")
print(first_name, middle_name, last_name, sep='*', end='#')
print("Hello! How are you?")

